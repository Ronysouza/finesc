  <h3>Missão</h3>
			<p>Promover o ensino e educação profissional de qualidade e inovadora, colocando no mercado de trabalho profissionais competentes e de qualidade, que promova a excelência humana, acadêmica, e profissional para o desenvolvimento de uma sociedade sustentável. </p>
			<p>Ser um centro educacional de referência, inovador em suas propostas e práticas pedagógicas, formando cidadãos de caráter e profissionais capacitados e empreendedores.

Atualmente forma uma parceria com o Ceunp/FINESC oferece dois Cursos Técnicos concomitante/subsequente no período noturno: Qualidade e Administração. Já no diurno, possui o EMIEP/Ensino Médio Integrado ao Profissionalizante, sendo três dias período integral e dois no período matutino. Os cursos do diurno são: Meio Ambiente e Comércio Exterior.</p>
   
   <h3>Nossa Filosofia</h3>
   <p>Perceber a educação como um processo constante e integral, oferecer ao educando oportunidades de desenvolver a autonomia, a criatividade, a criticidade e transformar o seu entorno, em uma busca constante de uma melhor qualidade de vida. Há importância do aluno construir seus saberes de forma coletiva, a partir dos saberes intrínsecos, vivenciando o respeito, a cooperação, a fraternidade e a responsabilidade, como valores essenciais para si e para a sociedade. O ser humano é na sua integridade fruto de suas interações com o meio em que vive, construindo o saber em suas vivências e aprendizados, capaz de evoluir e assim de proporcionar da melhor forma, um bem estar individual e do coletivo social. </p>




				
