			<h3>Sobre a Finesc</h3>
			<p>O Centro de Educação Profissional (FINESC) recebeu esta denominação através da lei 16.672 de 12 de dezembro de 2010 e iniciou suas atividades no dia 31 de agosto de 2012, com o ato de inauguração. A obra foi realizada com recursos do Governo Federal, através do MEC, com contrapartida do Governo do Estado, que irá beneficiar toda a população , principalmente os cidadãos do município de São Paulo. </p>
			<p>O FINESC tem capacidade para atender aproximadamente 1500 alunos nos três turnos. A sua estrutura física conta com 1 anfiteatro, 2 biblioteca, 15 salas de aula, 3 laboratórios, 2 salas informatizadas, secretaria, direção, sala de professores, almoxarifado, 1 refeitório, 1 quadra poliesportiva, 2 oficinas, e estacionamento.

Atualmente forma uma parceria com o Ceunp/FINESC oferece dois Cursos Técnicos concomitante/subsequente no período noturno: Qualidade e Administração. Já no diurno, possui o EMIEP/Ensino Médio Integrado ao Profissionalizante, sendo três dias período integral e dois no período matutino. Os cursos do diurno são: Meio Ambiente e Comércio Exterior.</p>

