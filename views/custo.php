<p>Cerca de 20% da energia consumida pela empilhadeira é voltada para superar a resistência ao rolamento dos pneus. Utilizando pneus com baixa resistência ao rolamento você utiliza menos energia para realizar o mesmo movimento, proporcionando assim redução do consumo de combustível de até 6% e ajudando também a reduzir a emissão de CO_para o ar.

O CSEasy, uma de nossas mais recentes inovações é um ótimo exemplo dessa economia. Em um teste realizado recentemente, o CSEasy teve uma economia de 12,8% no consumo de combustível comparado com competidor, graças ao composto de borracha que lhe confere baixa resistência ao rolamento. Neste mesmo teste

obteve-se ainda 40% mais de eficiência na operação com o mínimo de tempo gasto para a substituição dos pneus, afinal trata-se de um pneu superelástico que não precisa de prensa.</p>
